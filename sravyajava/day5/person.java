public class person {
    private String name;
    private int age;
    private String country;
    public void setName(String name)
    {
        this.name=name;

    }
    public String getName()
    {
        return name;
    }
    public void setAge(int age)
    {
        this.age=age;

    }
    public int getAge()
    {
        return age;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }


}
